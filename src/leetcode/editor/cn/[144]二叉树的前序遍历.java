import java.util.ArrayList;
import java.util.List;
import java.util.UUID;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 * int val;
 * TreeNode left;
 * TreeNode right;
 * TreeNode() {}
 * TreeNode(int val) { this.val = val; }
 * TreeNode(int val, TreeNode left, TreeNode right) {
 * this.val = val;
 * this.left = left;
 * this.right = right;
 * }
 * }
 */
class Solution144 {

    public static void main(String[] args) {
        Solution144 solution144 = new Solution144();
        List<Integer> integers = solution144.preorderTraversal(null);
        System.out.println(integers);

        UUID uuid = UUID.randomUUID();
        System.out.println(uuid);
        String s = uuid.toString().replaceAll("-", "");
        System.out.println(s);

        String replace = uuid.toString().replace("-", "");
        System.out.println(replace);
    }

    public List<Integer> preorderTraversal(TreeNode root) {
        List<Integer> integers = new ArrayList<>();
        add(root, integers);
        return integers;
    }

    public void add(TreeNode root, List<Integer> integers) {
        if (root == null) {
            return;
        }
        integers.add(root.val);
        add(root.left, integers);
        add(root.right, integers);
    }

    static class TreeNode {

        int val;
        TreeNode left;
        TreeNode right;

        TreeNode() {
        }

        TreeNode(int val) {
            this.val = val;
        }

        TreeNode(int val, TreeNode left, TreeNode right) {
            this.val = val;
            this.left = left;
            this.right = right;
        }
    }
}

